//! 音频处理阶段间传递的数据结构
//!
//! 定义流水线各阶段之间传递的中间数据结构，
//! 确保类型安全和清晰的接口契约。

const std = @import("std");

// ============================================================================
// WAV 文件信息
// ============================================================================

/// WAV 文件信息
pub const WavInfo = struct {
    sample_rate: u32,
    num_channels: u16,
    bits_per_sample: u16,
    num_samples: u32,
};

// ============================================================================
// 音频预处理结果
// ============================================================================

/// 音频预处理结果：Mel 频谱特征
pub const ProcessedAudio = struct {
    /// Mel 频谱 [n_mel_bins, n_frames]，值域已标准化
    data: []f32,
    n_mel_bins: u32,
    n_frames: u32,
    allocator: std.mem.Allocator,

    pub fn deinit(self: *ProcessedAudio) void {
        self.allocator.free(self.data);
    }
};

// ============================================================================
// 帧数据
// ============================================================================

/// 单帧音频数据（加窗后）
pub const Frame = struct {
    /// 帧数据（零填充到 FFT 大小）
    data: []f32,
    /// 实际有效长度（窗口长度）
    valid_len: usize,
};

/// 帧序列输出
pub const FrameOutput = struct {
    frames: []Frame,
    n_frames: usize,
};

// ============================================================================
// 频谱数据
// ============================================================================

/// 功率谱数据（单帧）
pub const PowerSpectrum = struct {
    /// 功率谱 [n_freqs]，n_freqs = n_fft/2 + 1
    data: []f32,
    n_freqs: usize,
};

// ============================================================================
// Mel 频谱
// ============================================================================

/// Mel 频谱数据
pub const MelSpectrum = struct {
    /// Mel 频谱 [n_mel_bins, n_frames]（mel-major 布局）
    data: []f32,
    n_mel_bins: u32,
    n_frames: u32,
};

// ============================================================================
// 编码器输出
// ============================================================================

/// 音频编码器输出嵌入
pub const AudioEmbedding = struct {
    /// 嵌入向量 [n_output_embd, n_tokens]
    data: []f32,
    n_embd: u32,
    n_tokens: u32,
};
